def draw_runes(count: int, allow_reversed: bool = True) -> list[dict]:
    pool = list(RUNES)
    drawn = random.sample(pool, min(count, len(pool)))
    result = []
    for rune in drawn:
        is_rev = allow_reversed and rune["can_reverse"] and random.random() < 0.25
        result.append({"rune": rune, "reversed": is_rev})
    return result


def personal_rune(life_path_number: int) -> dict:
    mapping = {11: 11, 22: 13, 33: 20}
    idx = mapping.get(life_path_number)
    if idx is None:
        idx = life_path_number if 1 <= life_path_number <= 24 else ((life_path_number - 1) % 24) + 1
    return RUNE_BY_INDEX.get(idx, RUNES[0])


def year_rune(year: int) -> dict:
    s = sum(int(d) for d in str(year))
    while s > 24:
        s = sum(int(d) for d in str(s))
    if s == 0:
        s = 1
    return RUNE_BY_INDEX.get(s, RUNES[0])
